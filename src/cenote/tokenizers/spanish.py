_TOKEN_RE = re.compile(r"[a-záéíóúüñ]+", re.IGNORECASE)


class SpanishTokenizer:
    """Stemming + stopword + accent-fold tokenizer for BM25 over Spanish text."""

    def __init__(self, strip_accents: bool = True) -> None:
        self._strip_accents = strip_accents
        self._stemmer = Stemmer.Stemmer("spanish")

    def tokenize(self, text: str) -> list[str]:
        lowered = text.lower()
        if self._strip_accents:
            lowered = _fold_accents(lowered)
        raw_tokens = _TOKEN_RE.findall(lowered)
        kept = [t for t in raw_tokens if t not in SPANISH_STOPWORDS]
        stems = self._stemmer.stemWords(kept)
        return [s for s in stems if s not in SPANISH_STOPWORDS]


def _fold_accents(text: str) -> str:
    nfd = unicodedata.normalize("NFD", text)
    return "".join(c for c in nfd if unicodedata.category(c) != "Mn")
